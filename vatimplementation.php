					<h5>General Principles of VAT</h5>
					<p>A value-added tax (VAT) is  levied on products at every point of sale where value has been added, starting from raw material and going all the way to final retail purchase. Ultimately, the consumer pays the VAT; buyers at earlier stages of production receive reimbursements for the previous VAT they've paid.</p>
					<h5>Input VAT</h5>
					<p>Input VAT is the value added tax added to the price when goods are purchased or services are rendered. If the buyer is registered in the VAT Register, the buyer can deduct the amount of VAT paid from his/her settlement with the tax authorities.</p>
					<h5>Output VAT</h5>
					<p>Output VAT is the value added tax calculated and charged on the sales of goods and services.</p>
					<h5>Exempt Supply</h5>
					<p>An exempt supply is a supply on which VAT is not charged and for which the related input VAT is not deductible.</p>
					<p>For example: bare land, local transport, the sale of residential property (second sale onwards) lease of the residential property and certain financial services.</p>
					<h5>Zero-rated supply</h5>
					<p>A zero-rated supply is a taxable supply on which VAT is charged at 0% and for which the related input VAT is deductible.</p>
					<p>For example exports, healthcare, education, international transport of passengers and goods, the first sale of residential property, medicine, and medical equipment, investment in gold, silver and platinum, crude oil & natural gas etc.</p>
					<h5>Standard Rate Supply</h5>
					<p>A taxable supply at the Standard Rate is a supply on which VAT is charged at 5% and for which the related input VAT is deductible. All items which are not coming under both exempted category, as well as zero-rated category, are coming under standard rated supplies</p>
					<h5>Reverse charge mechanism under UAE VAT</h5>
					<p>In the UAE VAT, the Reverse Charge Mechanism is applicable while importing goods or services from outside the GCC countries. Under this, the businesses will not have to physically pay VAT at the point of import.</p>
					<p>The responsibility for reporting of a VAT transaction is shifted from the seller to the buyer; under Reverse Charge Mechanism. Here the buyer reports the Input VAT (VAT on purchases) as well as the output VAT (VAT on sales) in their VAT return for the same quarter.</p>
					<p>The reverse charge is the amount of VAT one would have paid on that goods or services if one had bought it in the UAE. The importer has to disclose the amount of VAT under both Input VAT as well as Output VAT categories of the VAT return of that quarter.</p>
					<p>Reverse Charge Mechanism eliminates the obligation for the overseas seller to register for VAT in the UAE.
